package Les01.L01;

public class VarTypes_01 {
    public static void main(String[] args) {
        //----------numeric (מספרים שלמים)---------
        // 1 byte - from 0 to 255
        byte b = 2;
        // 2 byte
        short sh = 32766;
        // 4 byte
        int integer = 12;
        // 8 byte
        long l = 12;
        //----------numeric (מספרים עשרוניים)--------
        double db = 12.4;
        float f = 2.44f;
        //----------תווים-----------------------------
        // 2 byte
        char ch = 'c';
        //----------בוליאני---------------------------
        //can contain only `true` or `false`
        boolean bl = true;

        //****************ref type********************
        String s = "Hello";
    }





}
